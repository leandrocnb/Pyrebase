valor_1 = 100
valor_2 = 14

#Soma
valor_soma = valor_1 + valor_2
print('O valor da soma eh ', valor_soma)

#Subtração
valor_sub = valor_1 - valor_2
print('O valor da subtração eh ', valor_sub) 

#Divisão
valor_div = valor_1 - valor_2
print('O valor da divisão eh ', valor_div)

#Multiplicação
valor_mult = valor_1 * valor_2
print('O valor da multiplicação eh ', valor_mult)

#Exponenciação 
valor_expo = valor_1 ** valor_2
print('O valor da exponenciação eh ', valor_expo)

#Resto da divisão por 2
valor_resto = valor_1 % 2
if(valor_resto == 0):
    print(valor_1, 'eh par!')
else:
    print(valor_1, 'eh impar!')